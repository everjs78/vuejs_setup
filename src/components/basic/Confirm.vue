<script lang="ts">
import Vue from 'vue';

export default Vue.extend({
  name: 'Confirm',
  data: () => ({
    dialog: false,
    resolve: (result: boolean) => {},
    reject: (result: boolean) => {},
    message: '',
    title: '',
    options: {
      color: 'primary',
      width: 290,
      zIndex: 200,
    },
  }),
  methods: {
    open(title: string, message: string, options: any) {
      this.dialog = true;
      this.title = title;
      this.message = message;
      this.options = Object.assign(this.options, options);
      return new Promise((resolve: any, reject: any) => {
        this.resolve = resolve;
        this.reject = reject;
      });
    },
    agree() {
      this.resolve(true);
      this.dialog = false;
    },
    cancel() {
      this.resolve(false);
      this.dialog = false;
    },
  },
});
</script>
